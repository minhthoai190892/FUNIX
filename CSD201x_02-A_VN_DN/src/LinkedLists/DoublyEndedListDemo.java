package LinkedLists;

public class DoublyEndedListDemo {
    public static void main(String[] args) {
        DoublyEndedList dEndedList = new DoublyEndedList();
        dEndedList.addTail(1);
        dEndedList.addTail(19);
        dEndedList.addTail(10);
        dEndedList.addTail(19);
        dEndedList.addTail(11);
        dEndedList.addTail(12);
        System.out.println(dEndedList);
    }
}

